package basics;

public class Days {
	public static void main(String[] args)
	{
		// execute different blocks of codes based on a condition
		String dayOfWeek = "Tuesday";
		
		switch(dayOfWeek) 
		{
		case "Monday": 
			System.out.println("Its first working day! Monday...");
			break;
		case "Tuesday": 
			System.out.println("Lets work a little more bcz its still Tuesday");
			break;
		case "Wednesday": 
			System.out.println("Its mid of the week :) its Wednesday");
			break;
		case "Thursday": 
			System.out.println("Two days to go..its Thursday");
			break;
		case "Friday": 
			System.out.println("Last working day :) Yeah!! Friday is here");
			break;
		
		}
	}

}
